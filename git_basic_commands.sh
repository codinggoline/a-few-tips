# ======================== GIT COMMANDS =========================
git config # Ex: git config --global user.email johndoe@user.com

git init # Initialize a new git repository

git add # Ex: git add file.txt [Add files]

git clone
# git clone username@0.0.0.0:/path/to/folder
# git clone /path/to/folder [to clone a local repository]

git commit -m # Ex: git commit -m "Message" [validate modification]

git status # [change's list]

git push # Ex: git push origin master [push changes]

git checkout
# Ex: git checkout -b <my-branch> [Create and  switch to the new branch]
# Ex: git checkout <my-branch> [switch branch]

git remote
# git remote -v [lists remote repositories]
# git remote add origin <0.0.0.0>

git branch # Manage branches
# git branch [Lists branches]
# git branch -d <my-branch> [Delete branch]

git pull # Merges remote changes with local changes

git merge # Merge branch to the current branch
